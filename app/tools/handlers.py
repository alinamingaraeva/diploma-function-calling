"""Реализации функций-обработчиков для инструментов"""

# Имитация базы знаний (словарь - как простая БД)
KNOWLEDGE_BASE = {
    "как создать аккаунт": "Для создания аккаунта перейдите на главную страницу и нажмите 'Регистрация'",
    "как сбросить пароль": "На странице входа нажмите 'Забыли пароль?' и следуйте инструкциям",
    "стоимость подписки": "Базовый тариф - 500 руб/мес, Про - 1500 руб/мес, Бизнес - 5000 руб/мес",
    "техподдержка": "Связаться с техподдержкой можно по email: support@acme.com",
}

def search_knowledge_base(query: str, product: str = None) -> str:
    """
    Ищет информацию в базе знаний.
    
    Аргументы:
        query: поисковый запрос
        product: опциональное имя продукта
    
    Возвращает:
        строка с ответом из базы знаний
    """
    # Приводим запрос к нижнему регистру для поиска
    query_lower = query.lower()
    
    # Проверяем, есть ли точное совпадение в "базе знаний"
    for key, answer in KNOWLEDGE_BASE.items():
        if key in query_lower:
            # Если указан продукт, добавляем его в ответ
            if product:
                return f"[{product}] {answer}"
            return answer
    
    # Если ничего не нашли
    return "Извините, не удалось найти информацию по вашему запросу. Пожалуйста, переформулируйте вопрос или обратитесь в техподдержку."